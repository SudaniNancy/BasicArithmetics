import Foundation

public class ArithmeticService{
    
    public init() {}
    
    public func addition(number1: Int, number2: Int) -> Int{
        return number1 + number2
    }
    
    public func subtraction(number1: Int, number2: Int) -> Int{
        return number1 - number2
    }
}
